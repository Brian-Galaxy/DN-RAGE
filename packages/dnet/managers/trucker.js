trucker.showMenu = function(player, offersId) {
    if (!user.isLogin(player))
        return;
    methods.debug('trucker.showMenu', offersId);
    switch (offersId) {
        case 0:

            let tr1 = trucker.offersPool1.filter((item) => {
                try {
                    return !trucker.has('accept' + item[0]);
                }
                catch (e) {
                    return false;
                }
            });

            player.call('client:menuList:showTruckerOffersMenu', [tr1]);

            if (tr1.length < 20)
                trucker.generateOfferByType(40, 1);
            break;
        case 1:

            let tr2 = trucker.offersPool2.filter((item) => {
                try {
                    return !trucker.has('accept' + item[0]);
                }
                catch (e) {
                    return false;
                }
            });

            player.call('client:menuList:showTruckerOffersMenu', [tr2]);

            if (tr2.length < 20)
                trucker.generateOfferByType(40, 2);
            break;
        case 2:

            let tr3 = trucker.offersPool3.filter((item) => {
                try {
                    return !trucker.has('accept' + item[0]);
                }
                catch (e) {
                    return false;
                }
            });

            player.call('client:menuList:showTruckerOffersMenu', [tr3]);

            if (tr3.length < 20)
                trucker.generateOfferByType(40, 3);
            break;
    }
};

