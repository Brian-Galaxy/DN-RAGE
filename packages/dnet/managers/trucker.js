trucker.doneOffer = function (player, offerId) {
    if (!user.isLogin(player))
        return;

    if (!player.vehicle) {
        trucker.notify(player, 'Вы должны находиться в транспорте', '~r~Ошибка');
        return;
    }

    let offer = trucker.getOffer(offerId);
    if (offer == null) {
        user.reset(player, 'isStartTrucker');
        trucker.notify(player, 'Данного заказа не существует', '~r~Ошибка');
        return;
    }
    let price = offer[offer.length - 1];

    user.reset(player, 'isStartTrucker');
    if (offer.length == 10) {
        trucker.notify(player, `Вы доставили груз и заработали ~g~${methods.moneyFormat(price)}`, 'Доставка');
        user.giveJobSkill(player);
        user.giveJobMoney(player, price);
        user.addWorkExp(player, 7);
    }
    else {

        let trailerId = player.vehicle.getVariable('trailer');

        if (!trailerId) {
            trucker.notify(player, '~r~У Вас нет прицепа');
            trucker.removeOffer(offerId);
            return;
        }

        let trailer = mp.vehicles.at(player.vehicle.getVariable('trailer'));

        if (!vehicles.exists(trailer)) {
            trucker.removeOffer(offerId);
            return;
        }

        if (trailer.getVariable('trId') != offerId) {
            trucker.notify(player, '~r~Вы привезли другой заказ');
            vehicles.respawn(trailer);
            trucker.removeOffer(offerId);
            return;
        }

        let heal = trailer.bodyHealth / 1000;

        trucker.notify(player, `Вы доставили груз и заработали ~g~${methods.moneyFormat(price * heal)}\n~s~Состояние вашего груза: ~b~${heal * 100}%`, 'Доставка');
        if (heal * 100 < 100)
            player.notify(`~y~Штраф: ~s~${methods.moneyFormat(price - (price * heal))}`);
        user.addWorkExp(player, 15);
        vehicles.respawn(trailer);
        user.giveJobSkill(player);
        user.giveJobMoney(player, price * heal);
    }

    trucker.removeOffer(offerId);
};

trucker.stop = function (player, offerId) {
    if (!user.isLogin(player))
        return;

    user.reset(player, 'isStartTrucker');
    if (!player.vehicle) {
        trucker.notify(player, 'Вы должны находиться в транспорте', '~r~Ошибка');
        return;
    }

    let offer = trucker.getOffer(offerId);
    if (offer == null)
        return;

    let trailerId = player.vehicle.getVariable('trailer');

    if (!trailerId) {
        trucker.removeOffer(offerId);
        return;
    }

    let trailer = mp.vehicles.at(player.vehicle.getVariable('trailer'));
    if (!vehicles.exists(trailer)) {
        trucker.removeOffer(offerId);
        return;
    }

    vehicles.respawn(trailer);
    trucker.removeOffer(offerId);
};

trucker.trySpawnTrailer = function (player, offerId) {
    if (!user.isLogin(player))
        return;

    let offer = trucker.getOffer(offerId);
    if (offer == null) {
        trucker.notify(player, 'Данного заказа не существует', '~r~Ошибка');
        return;
    }

    let id = offer[0];
    let name = offer[1];
    let company = offer[2];
    let model = offer[3];
    let liv = offer[4];
    let cl1 = offer[5];
    let cl2 = offer[6];
    let x = offer[7];
    let y = offer[8];
    let z = offer[9];
    let rot = offer[10];
    if (methods.getListOfVehicleInRadius(new mp.Vector3(x, y, z), 5).length >= 1) {
        player.call('client:jobs:trucker:isSpawn', [false]);
        return;
    }

    vehicles.spawnCarCb(veh => {
        if (vehicles.exists(veh)) {
            veh.setVariable('trCompany', company);
            veh.setVariable('trName', name);
            veh.setVariable('trId', id);
            veh.setVariable('trOwner', user.getId(player));
        }
        player.call('client:jobs:trucker:isSpawn', [vehicles.exists(veh)]);
    }, new mp.Vector3(x, y, z - 1), rot, model, cl1, cl2, liv);
};

